#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*new;
	size_t	i;

	i = 0;
	new = (char *)b;
	while (i < len)
	{
		new[i] = c;
		i++;
	}
	return (&new[0]);
}
